document.getElementById('text-input').addEventListener('input', function() {
    const text = this.value;
    const resultDiv = document.getElementById('result');
    resultDiv.innerHTML = '';

    const fonts = {
        'Cursive': { 'a': 'ğ’¶', 'b': 'ğ’·', 'c': 'ğ’¸', 'd': 'ğ’¹', 'e': 'ğ‘’', 'f': 'ğ’»', 'g': 'ğ‘”', 'h': 'ğ’½', 'i': 'ğ’¾', 'j': 'ğ’¿', 'k': 'ğ“€', 'l': 'ğ“', 'm': 'ğ“‚', 'n': 'ğ“ƒ', 'o': 'ğ‘œ', 'p': 'ğ“…', 'q': 'ğ“†', 'r': 'ğ“‡', 's': 'ğ“ˆ', 't': 'ğ“‰', 'u': 'ğ“Š', 'v': 'ğ“‹', 'w': 'ğ“Œ', 'x': 'ğ“', 'y': 'ğ“', 'z': 'ğ“' },
        'Double-struck': { 'a': 'ğ•’', 'b': 'ğ•“', 'c': 'ğ•”', 'd': 'ğ••', 'e': 'ğ•–', 'f': 'ğ•—', 'g': 'ğ•˜', 'h': 'ğ•™', 'i': 'ğ•š', 'j': 'ğ•›', 'k': 'ğ•œ', 'l': 'ğ•', 'm': 'ğ•', 'n': 'ğ•Ÿ', 'o': 'ğ• ', 'p': 'ğ•¡', 'q': 'ğ•¢', 'r': 'ğ•£', 's': 'ğ•¤', 't': 'ğ•¥', 'u': 'ğ•¦', 'v': 'ğ•§', 'w': 'ğ•¨', 'x': 'ğ•©', 'y': 'ğ•ª', 'z': 'ğ•«' },
        'Old English': { 'a': 'ğ”', 'b': 'ğ”Ÿ', 'c': 'ğ” ', 'd': 'ğ”¡', 'e': 'ğ”¢', 'f': 'ğ”£', 'g': 'ğ”¤', 'h': 'ğ”¥', 'i': 'ğ”¦', 'j': 'ğ”§', 'k': 'ğ”¨', 'l': 'ğ”©', 'm': 'ğ”ª', 'n': 'ğ”«', 'o': 'ğ”¬', 'p': 'ğ”­', 'q': 'ğ”®', 'r': 'ğ”¯', 's': 'ğ”°', 't': 'ğ”±', 'u': 'ğ”²', 'v': 'ğ”³', 'w': 'ğ”´', 'x': 'ğ”µ', 'y': 'ğ”¶', 'z': 'ğ”·' },
    };

    for (const fontName in fonts) {
        let convertedText = '';
        for (const char of text.toLowerCase()) {
            convertedText += fonts[fontName][char] || char;
        }

        const fontDiv = document.createElement('div');
        fontDiv.className = 'mb-3';
        fontDiv.innerHTML = `
            <h5>${fontName}</h5>
            <textarea class="form-control" rows="2" readonly>${convertedText}</textarea>
        `;
        resultDiv.appendChild(fontDiv);
    }
}); 